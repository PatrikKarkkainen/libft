#include "libft.h"

char	*ft_strncpy(char *dest, const char *src, size_t n)
{
	int		i;

	i = 0;
	while (src[i] != '\0')
	{
		dest[i] = src[i];
		i++;
	}
	if ((size_t)i < n)
		dest[i] = '\0';
	while (dest[i] != '\0')
		i++;
	dest[i] = '\0';
	return (dest);
}
